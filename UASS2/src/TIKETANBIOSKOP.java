    public class Transaction {
    private String jumlahBeli;
    private int harga;
    private int pembayaran;
    private int totalBayar;
    private int kembalian;

    public Transaction(String jumlahBeli, int harga, int pembayaran, int totalBayar) {
        this.jumlahBeli = jumlahBeli;
        this.harga = harga;
        this.pembayaran = pembayaran;
        this.totalBayar = totalBayar;
    }

    public boolean isJumlahBeliValid() {
        jumlahBeli = jumlahBeli.trim();
        return !(jumlahBeli.isBlank() || jumlahBeli.isEmpty());
    }

    public void calculateTotalBayar() {
        int jumlah = Integer.parseInt(jumlahBeli);
        totalBayar = harga * jumlah;
    }

    public boolean isPembayaranSufficient() {
        return pembayaran >= totalBayar;
    }

    public int calculateKembalian() {
        kembalian = pembayaran - totalBayar;
        return kembalian;
    }

    public void showMessage(String message, String title) {
        JOptionPane.showMessageDialog(null, message, title, JOptionPane.INFORMATION_MESSAGE);
    }

    // Getters
    public int getTotalBayar() {
        return totalBayar;
    }

    public int getKembalian() {
        return kembalian;
    }
}
    
    
    
