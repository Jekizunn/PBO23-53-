    private void txt_pembayaranActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_txt_pembayaranActionPerformed
    String jumlahBeli = txt_jumlahbeli.getText();
    int harga = Integer.parseInt(txt_harga.getText());
    int pembayaran = Integer.parseInt(txt_pembayaran.getText());
    int totalBayar;

    if (txt_totalbayar.getText().isEmpty()) {
        totalBayar = 0;
    } else {
        totalBayar = Integer.parseInt(txt_totalbayar.getText());
    }

    Transaction transaction = new Transaction(jumlahBeli, harga, pembayaran, totalBayar);

    if (!transaction.isJumlahBeliValid()) {
        transaction.showMessage("Jumlah beli masih kosong", "ERROR");
        txt_jumlahbeli.setText("");
        txt_pembayaran.setText("");
    } else {
        transaction.calculateTotalBayar();
        txt_totalbayar.setText(String.valueOf(transaction.getTotalBayar()));

        if (!transaction.isPembayaranSufficient()) {
            transaction.showMessage("Pembayaran Kurang", "ERROR");
        } else {
            int kembalian = transaction.calculateKembalian();
            txt_kembalian.setText(String.valueOf(kembalian));
        }
    }
       
       
        
        

    }//GEN-LAST:event_txt_pembayaranActionPerformed

